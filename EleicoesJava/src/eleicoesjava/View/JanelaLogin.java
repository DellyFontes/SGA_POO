    private void btnEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEntrarActionPerformed
      String usuario = txtUsuario.getText().trim();
    String senha = new String(txtSenha.getPassword());
    
    if (usuario.isEmpty() || senha.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Preencha todos os campos!", "Erro", JOptionPane.ERROR_MESSAGE);
        return;
    }
    
    // Tentar autenticar usando cÃ³digo do funcionÃ¡rio como usuÃ¡rio
    if (AutenticacaoController.autenticar(usuario, senha)) {
        Funcionario user = AutenticacaoController.getUsuarioLogado();
        JOptionPane.showMessageDialog(this, 
            "Login realizado com sucesso!\nBem-vindo, " + user.getNome() + 
            "\nNÃ­vel de acesso: " + user.getNivelAcesso(), 
            "Sucesso", JOptionPane.INFORMATION_MESSAGE);
        new MenuPrincipal().setVisible(true);
        this.dispose();
    } else {
        JOptionPane.showMessageDialog(this, "CÃ³digo de funcionÃ¡rio ou BI invÃ¡lidos!", "Erro", JOptionPane.ERROR_MESSAGE);
        txtSenha.setText("");
        txtUsuario.requestFocus();
    }
    }//GEN-LAST:event_btnEntrarActionPerformed

